        [HttpDelete("DeleteData")]
        [ApiExplorerSettings(IgnoreApi = true)]
        public async Task<IActionResult> DeleteData([FromForm] string key)
        {
            logger.Debug($"string key = {key}");

            #region Validation

            dynamic cekTrans = dbContext.barge.Where(o => o.tug_id == key).Count();
            if (cekTrans > 0) return BadRequest("Can't be deleted, already been used in Barging.");

            cekTrans = dbContext.shipping_instruction.Where(o => o.tug_id == key).Count();
            if (cekTrans > 0) return BadRequest("Can't be deleted, already been used in Shipping Instruction.");

            #endregion

            try
            {
                var record = dbContext.tug
                    .Where(o => o.id == key)
                    .FirstOrDefault();
                if (record != null)
                {
                    dbContext.tug.Remove(record);
                    await dbContext.SaveChangesAsync();
                }

                return Ok();
            }
			catch (Exception ex)
			{
				logger.Error(ex.InnerException ?? ex);
                return BadRequest(ex.InnerException?.Message ?? ex.Message);
			}
        }

